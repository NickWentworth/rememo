'use client';

import { Close, Trash } from '@/components/icons';
import Button, { AddButton } from '@/components/Button';
import { DateTimePicker } from './DateTimePicker';
import { TaskPayload } from '@/lib/types';
import { todayUTC } from '@/lib/date';
import { FormState } from '@/lib/hooks/useFormState';
import { useAllCourses } from '@/lib/query/courses';
import { useTaskMutations } from '@/lib/query/tasks';
import { Controller, useFieldArray, useForm } from 'react-hook-form';
import { Subtask } from '@prisma/client';
import styles from './form.module.css';

const DEFAULT_TASK = {
    id: '',
    name: '',
    completed: false,
    due: todayUTC('23:59'),
    description: '',
    subtasks: [],
    courseId: null,
    course: null,
    userId: '',
} satisfies TaskPayload;

const DEFAULT_SUBTASK = {
    id: '',
    name: '',
    completed: false,
    due: todayUTC('23:59'),
    taskId: '',
} satisfies Subtask;

type TaskFormProps = {
    state: FormState<TaskPayload>;
    onCloseClick?: () => void;
};

export function TaskForm(props: TaskFormProps) {
    // reference all courses to optionally link a task to a course
    const { data: courses } = useAllCourses();

    const { create: createTask, update: updateTask } = useTaskMutations();

    // form data managed by useForm hook
    const {
        register,
        handleSubmit,
        control,
        reset,
        formState: { errors },
    } = useForm<TaskPayload>({
        values: props.state.mode === 'update' ? props.state.data : DEFAULT_TASK,
    });

    // dynamic subtasks section managed by useFieldArray hook
    const subtasksField = useFieldArray({
        control,
        name: 'subtasks',
    });

    if (props.state.mode === 'closed' || courses === undefined) {
        // return early if closed and render nothing
        return;
    }

    const title = props.state.mode === 'create' ? 'Add Task' : 'Modify Task';

    function onSubmit(data: Partial<TaskPayload>) {
        // common task data used when either creating or updating
        const partialTask = {
            // required task fields
            name: data.name!,
            due: data.due!,
            // optional task fields
            description: data.description ?? '',
            subtasks: data.subtasks ?? [],
            courseId: (data.courseId === '' ? null : data.courseId) ?? null,
            course: null,
        } satisfies Partial<TaskPayload>;

        switch (props.state.mode) {
            case 'closed':
                console.error('Task form is being submitted in closed state');
                break;

            case 'create':
                createTask({
                    ...partialTask,
                    id: '', // id will be auto-generated by prisma
                    userId: '', // user id will be added after verifying authorization
                    completed: false,
                });
                break;

            case 'update':
                const initial = props.state.data;
                updateTask({
                    ...partialTask,
                    id: initial.id,
                    completed: initial.completed,
                    userId: initial.userId,
                });
                break;
        }

        // close the form after submitting and reset fields
        props.onCloseClick?.();
        reset();
    }

    return (
        <div className={styles.fillPage}>
            <form className={styles.form} onSubmit={handleSubmit(onSubmit)}>
                {/* header section */}
                <div className={styles.formHeader}>
                    <h1>{title}</h1>

                    <Button
                        type='transparent'
                        onClick={props.onCloseClick}
                        icon={<Close size={30} color='white' />}
                    />
                </div>

                <hr />

                {/* main task data */}
                <div className={styles.formSection}>
                    {/* name */}
                    <div className={styles.fieldContainer}>
                        <label htmlFor='name'>
                            <p>Name</p>
                        </label>

                        <input
                            type='text'
                            id='name'
                            {...register('name', {
                                required: 'Task must include a name',
                            })}
                        />

                        <p className={styles.error}>{errors.name?.message}</p>
                    </div>

                    {/* course */}
                    <div className={styles.fieldContainer}>
                        <label htmlFor='course'>
                            <p>Course</p>
                        </label>

                        <select id='course' {...register('courseId')}>
                            {/* IMPORTANT: remember to convert (null <=> '') */}
                            <option value=''>None</option>

                            {courses.map((c) => (
                                <option
                                    key={c.id}
                                    value={c.id}
                                    style={{ color: c.color }}
                                >
                                    {c.name}
                                </option>
                            ))}
                        </select>
                    </div>

                    {/* due date */}
                    <div className={styles.fieldContainer}>
                        <label htmlFor='due'>
                            <p>Due</p>
                        </label>

                        <Controller
                            control={control}
                            name='due'
                            render={({ field }) => (
                                <DateTimePicker
                                    value={field.value}
                                    onChange={field.onChange}
                                />
                            )}
                        />
                    </div>
                </div>

                <hr />

                {/* subtasks */}
                <div className={styles.formSection}>
                    <div className={styles.fieldContainer}>
                        <label>
                            <p>Subtasks</p>
                        </label>

                        {subtasksField.fields.map((field, idx) => (
                            <div key={field.id} className={styles.subtaskRow}>
                                <input
                                    type='text'
                                    className={styles.subtaskRowName}
                                    {...register(`subtasks.${idx}.name`, {
                                        required: 'Subtask must include a name',
                                    })}
                                />

                                <Controller
                                    control={control}
                                    name={`subtasks.${idx}.due`}
                                    render={({ field }) => (
                                        <DateTimePicker
                                            onChange={field.onChange}
                                            value={field.value}
                                        />
                                    )}
                                />

                                <Button
                                    type='transparent'
                                    onClick={() => subtasksField.remove(idx)}
                                    icon={<Trash color='light' size={16} />}
                                />

                                <p
                                    key={`${field.id}ERROR`}
                                    className={styles.error}
                                >
                                    {errors.subtasks?.at?.(idx)?.name?.message}
                                </p>
                            </div>
                        ))}

                        <div className={styles.alignCenter}>
                            <AddButton
                                onClick={() =>
                                    subtasksField.append(DEFAULT_SUBTASK)
                                }
                            />
                        </div>
                    </div>
                </div>

                <hr />

                {/* description */}
                <div className={styles.formSection}>
                    <div className={styles.fieldContainer}>
                        <label htmlFor='description'>
                            <p>Description (optional)</p>
                        </label>

                        <textarea
                            id='description'
                            {...register('description')}
                            rows={4}
                        />
                    </div>
                </div>

                <hr />

                <div className={styles.alignCenter}>
                    <Button type='solid' submit>
                        Submit
                    </Button>
                </div>
            </form>
        </div>
    );
}
